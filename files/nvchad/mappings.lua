-- Neovim key mappings

-- Normal mode mappings
vim.keymap.set("n", "<C-k>", function()
  vim.lsp.buf.format()
end, { desc = "Format buffer" })

vim.keymap.set("n", "Y", ":%y+ <CR>", { desc = "Yank whole file" })
vim.keymap.set("n", "<C-t>", function()
  require("nvchad.themes").open()
end, {})
-- vim.keymap.set("n", "<C-t>", ":%s/<C-r><C-w>//gc<Left><Left><Left>", { desc = "Replace word under cursor (confirmation)" })
vim.keymap.set("n", "<C-r>", ":%s/<C-r><C-w>//gc<Left><Left><Left>", { desc = "Replace word under cursor with onfirmation" })
vim.keymap.set("n", "<C-d>", "<ESC>^dd", { desc = "Delete whole line" })
vim.keymap.set("n", "<C-/>", function()
  require("Comment.api").toggle.linewise.current()
end, { desc = "Comment line" })
vim.keymap.set("n", "<C-n>", ":NvimTreeToggle<CR>", { desc = "Toggle tree" })
vim.keymap.set("n", "<C-;>", ":m .-2<CR>==", { desc = "Move line up" })
vim.keymap.set("n", "<C-s>", "<ESC> <cmd> w <CR>", { desc = "Save the file" })
vim.keymap.set("n", "<C-h>", "<cmd>NvimTreeFocus<CR>", { desc = "Focus on the tree" })
vim.keymap.set("n", "<C-l>", "<cmd>wincmd l<CR>", { desc = "Focus on the buffer" })
vim.keymap.set("n", "<A-Up>", ":m .-2<CR>==", { desc = "Move line up" })
vim.keymap.set("n", "<C-'>", ":m .+1<CR>==", { desc = "Move line down" })
vim.keymap.set("n", "<A-Down>", ":m .+1<CR>==", { desc = "Move line down" })
vim.keymap.set("n", "<C-o>", ":Telescope find_files <CR>", { desc = "Find all files" })
vim.keymap.set("n", "<C-p>", ":Telescope git_files <CR>", { desc = "Find files added to Git" })
-- vim.keymap.set("n", "<C-p>", ":Tv <CR>", { desc = "Find files added to Git" })
vim.keymap.set("n", "<C-z>", "<cmd> undo <CR>", { desc = "Undo" })
vim.keymap.set("n", "<leader>gs", ":Telescope git_status <CR>", { desc = "Git status" })
vim.keymap.set("n", "<leader>gg", ":LazyGit <CR>", { desc = "LazyGit" })
vim.keymap.set("n", "<C-f>", ":Telescope live_grep <CR>", { desc = "live grep" })
vim.keymap.set("n", "<C-b>", "<ESC>^i", { desc = "beginning of line" })
vim.keymap.set("n", "<A-Left>", "<ESC>^", { desc = "beginning of line" })
vim.keymap.set("n", "<A-Right>", "<End>", { desc = "end of line" })
vim.keymap.set("n", "<C-S-d>", "<ESC> yyp", { desc = "Duplicate line" })
vim.keymap.set("n", "<C-e>", "<End>", { desc = "end of line" })
vim.keymap.set("n", ":", "<cmd>FineCmdline<CR>", { desc = "CmdLine" })
vim.keymap.set("n", "q", function()
  require("nvchad.tabufline").close_buffer()
end, { desc = "Close current buffer" })

-- Visual mode mappings
vim.keymap.set("v", "<A-Up>", ":m '<-2<CR>gv=gv", { desc = "Move block up" })
vim.keymap.set("v", "<A-Down>", ":m '>+1<CR>gv=gv", { desc = "Move line down" })
vim.keymap.set("v", "<C-/>", "gc", { desc = "Comment selection" })

-- Insert mode mappings
vim.keymap.set("i", "<C-BS>", "<C-w>", { desc = "Delete previous word" })
vim.keymap.set("i", "<A-Up>", "<ESC> :m .-2<CR>== i", { desc = "Move line up" })
vim.keymap.set("i", "<A-Down>", "<ESC> :m .+1<CR>== i", { desc = "Move line down" })
vim.keymap.set("i", "<A-Left>", "<ESC>^i", { desc = "beginning of line" })
vim.keymap.set("i", "<A-Right>", "<End>", { desc = "end of line" })
vim.keymap.set("i", "<C-s>", "<ESC> <cmd> w <CR> i", { desc = "Save" })
vim.keymap.set("i", "<C-z>", "<ESC> <cmd> undo <CR> i", { desc = "Undo" })
vim.keymap.set("i", "<C-S-d>", "<ESC> yyp i", { desc = "Duplicate line" })
